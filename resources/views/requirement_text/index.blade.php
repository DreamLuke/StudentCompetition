                    соавторов — фамилия первого автора и обозначение «и др.»), б) через указания в скобках номера
                    источника в списке литературы. ФИО редко цитируемых зарубежных авторов желательно приводить
                    дважды — как в русской транскрипции, так и на языке оригинала.
                </p>
                <p>
                    6) Возможны два способа цитирования:
                    а) прямое цитирование, в этом случае в кавычках дословно повторяется текст из соответствующего
                    б) косвенное цитирование, когда одна или несколько мыслей, возможно из разных мест цитируемого
                    источника излагаются автором своими словами, но более или менее близко к оригинальному тексту.
                </p>
                <p>
                    7) В соответствии с этикой научного изложения, необходимо строго следить за правильностью
                    цитирования и соответствия ссылок на источники, по возможности разделять упоминаемые в работе
