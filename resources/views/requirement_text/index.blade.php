                    Ф.И.О.), внизу страницы — место и год проведения работы. (точную форму, установленную для
                    определенного ВУЗа, можно найти у методиста, закрепленного за Вами)
                </p>
                <p>
                    2) Оглавление. (оформление оглавления также различается. Возможно употребление терминов: план,
                    содержание, оглавления. Иногда необходимо указывать страницы, соответствующие разделам, иногда
                    нет подобных требований)
                </p>
                <p>
                    3) Введение (1-4 стр.), где должно быть кратко сформулированы: актуальность, предмет, объект,
                    цель, задачи исследования, фундаментальная проблема, которой посвящена работа, частные проблемы,
                    основные подходы и идеи, предлагаемые автором для решения поставленной задачи, методика
                    исследования, обзор литературы, которая будет использоваться при написании работы. Знакомясь с
