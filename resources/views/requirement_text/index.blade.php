                    практикой мировой науки) можно считать трехчастную форму. Три одинаково важных основных раздела:
                </p>
                <p
                >1) введение в проблему исследования (литературный обзор) с постановкой целей и задач, разработкой
                    программы исследования,
                </p>
                <p>
                    2) описание и анализ собственных результатов и
                </p>
                <p>
                    3) обсуждение результатов в соответствии с исходными целями и задачами — структурируют изложение
                    в логике, удобной для понимания сути научного исследования.
                </p>
                <p>
                    Иногда в работе выделяют теоретическую и практическую части, что не соответствуют логике научного
                    изложения, поскольку не так существенно, каким именно способом получен научный результат — на
                    основе теоретического обобщения уже известных фактов (возможны работы с использованием логического
