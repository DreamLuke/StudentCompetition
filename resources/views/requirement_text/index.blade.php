                    данная работа, стоит ли ее читать далее полностью.
                </p>
                <p>
                    4) Основная часть которая заключается в анализе проблемы исследования, при этом желательно, чтобы
                    освещались: исторический аспект исследования вопроса, выяснение теоретических проблем исследования
                    (проблемы с определениями основных понятий, затронутых в исследовании и т.д.), практические
                    проблемы вопроса исследования.
                </p>
                <p>
                    5) Заключение (2-8 страниц в зависимости от типа работы). В данном разделе подробно описываются
                    и анализируются все полученные данные и выявленные закономерности. Изложение материала
                    структурируется исходя из логики решения проблемы в соответствии с экспериментальным планом. В
                    целом данный раздел должен предоставить читателю исчерпывающую информацию обо всех выявленных в
                    данной работе фактах и закономерностях и заканчиваться кратким заключением, в котором можно сделать
                    предварительные выводы относительно главного тезиса, выносимого на защиту.
                </p>
                <p>
                    6) Приложение (необязательный раздел, объем которого не входит в расчет общего объема работы, но
                    использовать данный раздел, желательно. Если данный раздел есть, то он должен быть хорошо
                    структурирован и оформлен). В данном разделе выносятся все возможные схемы, таблицы, графики,
                    демонстрирующие текст работы. Приводимые данные (целесообразно обобщать их в виде схем, графиков и
                    таблиц) должны доказывать или опровергать выдвинутые ранее гипотезы. На схемах и графиках (или в
                    подписях к ним) необходимо подробно разъяснять все условные знаки и обозначения, что отложено по
                    осям и т.п. Каждый график целесообразно дублировать (в приложении) таблицей с тем, чтобы дать
                    возможность читателю, если его заинтересуют представленные данные, использовать их и для других
                    целей, например, для проверки других зависимостей, не упомянутых в данном разделе. В начале
                    приложения необходимо давать общий список всех приложений (рисунков, таблиц, протоколов и других
                    материалов). Каждое приложение должно сопровождаться не только номером и названием, но и
                    необходимыми комментариями (подписями), расшифровывающими аббревиатуры. Читатель должен иметь всю
                    необходимую информацию, чтобы разобраться в материалах приложения без обращения к основному тексту
                    работы.
                </p>
                <p>
                    7) Список литературы (объем зависит от количества цитированных источников). Целесообразно
                    использовать тот же размер шрифта и межстрочных интервалов, как и в основном тексте; каждый
                    источник описывается на отдельном абзаце. Литературные источники должны быть представлены таким
                    образом, чтобы можно было найти сам источник и в нем нужную информацию. С этой целью для каждого
                    источника необходимо указать: фамилии и инициалы всех авторов, название работы, название журнала,
                    его выходные данные (том, номер и др.), название сборника, фамилии его редакторов или составителей
                    и его дополнительные выходные данные, для всех источников место, издательство (для не периодических
                    изданий) и год издания, страницы (если ссылаются на статью, главу в сборнике или на тезисы доклада,
                    то указываются страницы начала и конца статьи, если же ссылаются на всю книгу или монографию
                    целиком, то указывается общий объем страниц). Источники должны быть упорядочены и пронумерованы
                    (источники на русском языке вначале, а затем все источники на иностранных языках); порядок
                    следования работ в списке литературы — по алфавиту на основании фамилии первого (или единственного)
                    автора, если несколько источников одного автора, — то по фамилии следующего соавтора (если есть)
                    или по дате издания.»
                </p>
