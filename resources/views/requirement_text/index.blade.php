                    результаты изменяют состояние проблемы исследования).
                </p>
                <p>
                    Таким образом, можно рекомендовать следующую типовую структуру научной студенческой работы:
                </p>
                <p>
                    1) Титульный лист. На титульном листе сверху указывается организация, где выполнена работа, в
